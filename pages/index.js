import Head from "next/head";
import styled from "styled-components";
import { useState } from "react";
import CreateFlowForm from "/components/CreateFlowForm";
import FlowCard from "/components/FlowCard";
import { getAllFlows } from "/services/flowService";
import { useRouter } from "next/router";
import { useEffect } from "react";
import Image from "next/image";

export async function getServerSideProps() {
  const flowsDB = await getAllFlows();
  return {
    props: { flowsDB: flowsDB },
  };
}

export default function Home({ flowsDB }) {
  const [openForm, setOpenForm] = useState(false);
  const [editFormId, setEditFormId] = useState(null);
  const [isUpdating, setIsUpdating] = useState(false);

  const router = useRouter();
  const refreshData = () => {
    router.replace(router.asPath);
    setIsUpdating(true);
  };
  useEffect(() => {
    setIsUpdating(false);
  }, [flowsDB]);

  function toggleOpenForm() {
    setOpenForm((prev) => !prev);
  }

  async function handleFlowPost(flowData) {
    try {
      const response = await fetch("/api/flows", {
        method: "POST",
        body: JSON.stringify(flowData),
      });

      const result = await response.json();

      /* if (result.createdId) {
        alert("Flow has been created");
      } else {
        alert("Creating a flow did not work!!");
      } */
    } catch (error) {
      console.error(error);
    }
    setOpenForm(false);
    refreshData();
  }

  async function handleFlowUpdate(flowData) {
    try {
      const response = await fetch("/api/flows", {
        method: "PATCH",
        body: JSON.stringify(flowData),
      });

      const result = await response.json();

      /*  if (result.name) {
        alert("Flow has been updated");
      } else {
        alert("Updating a flow did not work!!");
      } */
    } catch (error) {
      console.error(error);
    }
    setOpenForm(false);
    setEditFormId(null);
    refreshData();
  }

  async function handleDelete(id) {
    try {
      const response = await fetch(`/api/flows`, {
        method: "DELETE",
        body: JSON.stringify(id),
      });

      const result = await response.json();
      /* 
      if (result.id) {
        alert("Flow has been deleted");
      } else {
        alert("Deleting a flow did not work!!");
      } */
    } catch (error) {
      console.error(error);
    }
    refreshData();
  }

  function cancelEditFlow() {
    setEditFormId(null);
  }

  function closeForm() {
    setOpenForm(false);
  }

  return (
    <div>
      <Head>
        <title>My App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <StyledH2>NAMASTE</StyledH2>
        <h3>Let&apos;s flow together</h3>

        <Image
          src="/images/animation-start-s2.gif"
          width={523}
          height={523}
          alt="yoga"
          layout="responsive"
        />
        <StyledWrapper>
          <h2>Your Flows: </h2>
          <StyledParagraph>Choose a flow or create a new one</StyledParagraph>
          {flowsDB.map((flow) => (
            <FlowCard
              key={flow.id}
              name={flow.name}
              hours={flow.hours}
              minutes={flow.minutes}
              id={flow.id}
              deleteFlow={() => handleDelete(flow.id)}
              setEditFormId={() => setEditFormId(flow.id)}
            />
          ))}
          {openForm && (
            <CreateFlowForm
              flows={flowsDB}
              handleFlowPost={handleFlowPost}
              closeForm={closeForm}
            />
          )}
          {editFormId != null &&
            flowsDB.map(
              (flow) =>
                flow.id === editFormId && (
                  <CreateFlowForm
                    key={flow.id}
                    flows={flowsDB}
                    id={flow.id}
                    editFormId={editFormId}
                    defaultName={flow.name}
                    defaultHours={flow.hours}
                    defaultMinutes={flow.minutes}
                    handleFlowUpdate={handleFlowUpdate}
                    cancelEditFlow={cancelEditFlow}
                  />
                )
            )}
          <StyledAddButton onClick={toggleOpenForm}>
            {openForm ? "x" : "+"}
          </StyledAddButton>
        </StyledWrapper>
      </main>
    </div>
  );
}

const StyledAddButton = styled.button`
  position: fixed;
  bottom: 3rem;
  right: 2rem;
  z-index: 30;
  border: none;
  display: block;
  margin: auto;
  background: var(--highlight-gradient);
  box-shadow: var(--drop-shadow-gray);
  color: var(--text-light);
  font-size: 2rem;
  width: 4rem;
  height: 4rem;
  border-radius: 50%;
  text-align: center;
  cursor: pointer;
  &:active {
    background-color: var(--highlight);
    color: var(--text-light);
  }
`;

const StyledH2 = styled.h2`
  font-size: 2rem;
`;

const StyledParagraph = styled.p`
  text-align: center;
`;

const StyledWrapper = styled.section`
  margin-top: 3rem;
`;
